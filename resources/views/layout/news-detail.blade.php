                        <p>It has been more than 30 years since some ghosts were busted on the big screen, and you cannot wait to see the new ‘Ghostbusters’ in the cinema. The movie will be released at the end of this month. The 2016 ‘Ghostbusters’ will treat its audiences with a totally new team of courageous Ghostbusters.  Before enjoying the popcorn and the big screen, you might want to know some interesting must-know details about the movie.</p>
                        <p> </p>
                        <p><strong>1. This is a Reboot, not a Sequel</strong></p>
                        <p style="text-align:center"><img alt="" src="http://img.123movies.to/2016/07/23/cover/anh1.jpg" /></p>
                        <p>After countless false attempts to make Ghostbusters 3 into reality, Sony Pictures finally gave up the idea of making a sequel to the last two Ghostbusters (released in 1984 and 1998). Instead, a total reboot was started in 2014. With the addition of director Paul Feig, Sony Pictures can finally take the project out of the development hell and become a reality.</p>
                        <p> </p>
                        <p><strong>2. It Almost Features all New Characters</strong></p>
                        <p>Even the new Ghostbusters is a reboot of the franchise, you should not expect it to feature all its lineup of characters or events of the original in 1984. There will not be any familiar character like Ray, Egon, Peter or Winston around this time. (However, you still can see a bit of original surviving cast members in this new movie).  </p>
                        <p style="text-align:center"><img alt="" src="http://img.123movies.to/2016/07/23/cover/anh2.jpg" /></p>
                        <p>Instead, in this Ghostbusters reboot you will meet an all-girl ghostbusters. The movie revolves around two disgraced believers in the paranormal. They are Abby Yates (Melissa McCarthy) and Erin Gilbert (Kristen Wiig) – scientists who want to prove the existence of ghosts. After shaking hands with weird and kooky nuclear technician named Jillian Holtzmann (Kate McKinnon) and a subway worker named Patty Tolan (Leslie Jones) and a secretary named Kevin (Chris Hemsworth), those women are then inspired to form the all new Ghostbusters.</p>
                        <p style="text-align:center"><img alt="" src="http://img.123movies.to/2016/07/23/cover/anh3.jpg" /></p>
                        <p>The villain is different this time in Ghostbusters 2016. Instead of Gozer in the ancient legend, a new character of Rowan (Neil Casey) is introduced. Rowan is a powerful demon who wants to unleash a ghostly apocalypse upon Manhattan. But don’t worry, there is one familiar face, it is the green ghost Slimer. It will return to make life difficult for those Ghostbusters.</p>
                        <p> </p>
                        <p><strong>3. Ghostbusters 2016 Does not Always Get Positive Reactions from Audiences</strong></p>
                        <p>Many people though that it might be very exciting to have a reboot Ghostbusters after 25 years of waiting. But you are wrong. Some Walter Pecks-in-training were dead set against the project before the first images and footage even appeared. And when the first trailer did hit the Internet, it quickly became the most disliked movie trailer in YouTube history.</p>
                        <p style="text-align:center"><img alt="" src="http://img.123movies.to/2016/07/23/cover/anh4.jpg" /></p>
                        <p>Why there are such negative reactions when Ghostbusters is not the first movie from the 80s to get a reboot/ remake? Most of the reasons come from the female-dominated cast. But Sony is positive. In fact, Tom Rothman, the Sony studio chief, called this online controversy "the greatest thing that ever happened" as far as the new movie is concerned. They do say that any publicity is good publicity.</p>
                        <p> </p>
                        <p><strong>4. The Original Actors Will Have Cameos</strong></p>
                        <p style="text-align:center"><img alt="" src="http://img.123movies.to/2016/07/23/cover/anh4.1.jpg" /></p>
                        <p>This is a reboot, but it doesn’t mean that we don’t get to see some familiar old characters. Almost all surviving ghostbusters in the original movie are invited to become camoes, the original cast including Dan Aykroyd, Bill Murray, Ernie Hudson and Annie Potts.  Even they will not play their original characters, yet it is a good thing to see the old gang back together (minus the late Harold Ramis and retired Rick Moranis).</p>
                        <p style="text-align:center"><img alt="" src="http://img.123movies.to/2016/07/23/cover/anh4.2.jpg" /></p>
                        <p>The presence of Bill Murray is very notable since he was very reluctant to appear in another Ghostbusters movie. This is one of the main reasons why Ghostbusters 3 never happened. When Bill Murray told Vulture, he will be back in Ghostbusters 2016, and it is a great support to the new casts.</p>
                        <p> </p>
                        <p><strong>5. There Might Be a Ghostbusters Shared Univers</strong></p>
                        <p>Marvel has made shared movie universes very popular now and there is a chance that Ghostbusters will take part in this trend.</p>
                        <p style="text-align:center"><img alt="" src="http://img.123movies.to/2016/07/23/cover/anh5.jpg" /></p>
                        <p>As revealed in 2015, there is another Ghostbusters is in the making. The project is produced and directed by The Russo brothers (Captain America: Civil War) and rumored that it will features all-male team including Channing Tatum and Chris Pratt. The plan is to introduce the two separate teams and then they will team up for a larger shared universe full of weird and wacky ghostbusters.</p>
